// Loops are used to execute a block of code repeatedly as long as certain comsdition is true

// for loop : The for loop is used when i know how many times to execute a statement or a block of code

#include <iostream>
using namespace std;

int main()
{
    for (int i = 1; i <= 10; i++)
    {
        cout << "Number : " << i << endl;
    }
    return 0;
}